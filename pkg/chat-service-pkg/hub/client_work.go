package hub

import (
	"2019_1_TheBang/config"
	"encoding/json"
	"github.com/gorilla/websocket"
	"time"
)

const (
	writeWait      = 10 * time.Second
	pongWait       = 60 * time.Second
	pingPeriod     = (pongWait * 9) / 10
	maxMessageSize = 512
)

var (
	newline = []byte{'\n'}
	space   = []byte{' '}
)

func (c *Client) ReadPump() {
	defer func() {
		c.Hub.Unregister <- c
		c.Conn.Close()
	}()

	c.Conn.SetReadLimit(maxMessageSize)
	c.Conn.SetReadDeadline(time.Now().Add(pongWait))
	c.Conn.SetPongHandler(func(string) error { c.Conn.SetReadDeadline(time.Now().Add(pongWait)); return nil })
	for {
		_, bytes, err := c.Conn.ReadMessage()
		if err != nil {
			break
		}

		msg := messageFromClient{}
		if err := json.Unmarshal(bytes, &msg); err != nil {
			config.Logger.Info("invalid msg stuct (Unmarshal)")

			continue
		}

		msg.Author = c.Nickname
		msg.PhotoURL = "http://95.163.212.32:8001/icon/" + c.PhotoURL

		ok := InserMessage(msg)
		if !ok {
			config.Logger.Warn("msg was not saved")
		}

		if bytes, err = json.Marshal(msg); err != nil {
			config.Logger.Info("invalid msg stuct (Marshal)")

			continue
		}

		c.Hub.Broadcast <- bytes
	}
}

func (c *Client) WritePump() {
	ticker := time.NewTicker(pingPeriod)
	defer func() {
		ticker.Stop()
		c.Conn.Close()
	}()

	for {
		select {
		case message, ok := <-c.Send:
			c.Conn.SetWriteDeadline(time.Now().Add(writeWait))
			if !ok {
				// The hub closed the channel.
				c.Conn.WriteMessage(websocket.CloseMessage, []byte{})
				return
			}

			w, err := c.Conn.NextWriter(websocket.TextMessage)
			if err != nil {
				return
			}
			w.Write(message)

			n := len(c.Send)
			for i := 0; i < n; i++ {
				w.Write(newline)
				w.Write(<-c.Send)
			}

			if err := w.Close(); err != nil {
				return
			}

		case <-ticker.C:
			c.Conn.SetWriteDeadline(time.Now().Add(writeWait))
			if err := c.Conn.WriteMessage(websocket.PingMessage, nil); err != nil {
				return
			}
		}
	}
}
